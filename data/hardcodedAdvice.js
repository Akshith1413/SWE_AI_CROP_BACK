// Normalize crop and disease names for matching
function normalizeName(name) {
    return name.toLowerCase().trim();
}

// Get advice for a specific crop and disease
export function getHardcodedAdvice(language, crop, disease) {
    const normalizedCrop = normalizeName(crop);
    const normalizedDisease = normalizeName(disease);

    // Get language data or fall back to Hindi
    const langData = hardcodedAdvice[language] || hardcodedAdvice['hi'];

    // Try to find exact crop match
    const cropData = langData[normalizedCrop];

    if (cropData) {
        // Try to find exact disease match
        const diseaseData = cropData[normalizedDisease];
        if (diseaseData) {
            return diseaseData;
        }
    }

    // Return default advice if no match found
    return langData.default || hardcodedAdvice['hi'].default;
}

